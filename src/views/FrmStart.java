    /**
     * Método que se encarga de guardar los datos en la base de datos.
     * @param evt No implementado.
     */
    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        if (BtnSave.getText().equals("Nuevo")) {
            TxtDocument.setEnabled(true);
            TxtFullname.setEnabled(true);
            TxtEmail.setEnabled(true);
            TxtSmartphone.setEnabled(true);
            BtnSave.setText("Guardar");
            BtnCancel.setEnabled(true);
            TxtDocument.requestFocus();
        } else {
            // Falta implementar validación de campos.
            
            // Si los campos estan validados y son true debe guardar el contacto.
            Contacts contact = new Contacts(
                    TxtDocument.getText(),
                    TxtFullname.getText(),
                    TxtEmail.getText(),
                    TxtSmartphone.getText()
            );
            if (c.saveContact(contact)) {
                JOptionPane.showMessageDialog(null, "Registro guardado correctamente.");
                dataTable();
                startBtn();
                
            } else {
                JOptionPane.showMessageDialog(null, "El registro NO se guardo. Intente de nuevo.");
                
            }
            
        }
    }//GEN-LAST:event_BtnSaveActionPerformed

