    /**
     * Este método se encarga de obtener la información de la tabla para posiblemente editar.
     * @param evt Evento de mouse presionado, obtiene fila (row)
     */
    private void TblShowMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblShowMousePressed
        int row = TblShow.rowAtPoint(evt.getPoint());
        TxtDocument.setText(String.valueOf(TblShow.getValueAt(row, 2)));
        TxtFullname.setText(String.valueOf(TblShow.getValueAt(row, 3)));
        TxtEmail.setText(String.valueOf(TblShow.getValueAt(row, 4)));
        TxtSmartphone.setText(String.valueOf(TblShow.getValueAt(row, 5)));
        
        LblIdContact.setText(String.valueOf(TblShow.getValueAt(row, 0)));
        
        BtnSave.setEnabled(false);
        BtnUpdate.setEnabled(true);
        BtnDelete.setEnabled(true);
        BtnCancel.setEnabled(true);
    }//GEN-LAST:event_TblShowMousePressed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        //Eliminar registro
        int id = Integer.parseInt(LblIdContact.getText());
        if (c.deleteContact(id)) {
            JOptionPane.showMessageDialog(null, "Registro eliminado correctamente.");
                
        } else {
            JOptionPane.showMessageDialog(null, "El registro NO se eliminó. Intente de nuevo.");
                
        }
        
        dataTable();
        startBtn();
    }//GEN-LAST:event_BtnDeleteActionPerformed

