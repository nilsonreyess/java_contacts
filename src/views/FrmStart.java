    /**
     * Este método se encarga de poblar la tabla con los contactos.
     */
    private void dataTable() {
        ArrayList<Contacts> contacts = c.getContacts();
        DefaultTableModel mdlTable = new DefaultTableModel();
        
        mdlTable.addColumn("Id");
        mdlTable.addColumn("It");
        mdlTable.addColumn("Documento");
        mdlTable.addColumn("Nombre completo");
        mdlTable.addColumn("Correo electrónico");
        mdlTable.addColumn("Celular");
        
        TblShow.setModel(mdlTable);
        int it = 1;
        for (Contacts item : contacts) {
            Object[] itemTabla = new Object[6];
            itemTabla[0] = item.getId();
            itemTabla[1] = it;
            itemTabla[2] = item.getDocument();
            itemTabla[3] = item.getFullname();
            itemTabla[4] = item.getEmail();
            itemTabla[5] = item.getSmartphone();
            mdlTable.addRow(itemTabla);
            it++;
        }
        
        TblShow.setModel(mdlTable);
        
        TblShow.getColumnModel().getColumn(0).setMaxWidth(0);
        TblShow.getColumnModel().getColumn(0).setMinWidth(0);
        TblShow.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
        TblShow.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
        
        TblShow.getColumnModel().getColumn(1).setPreferredWidth(20);
        TblShow.getColumnModel().getColumn(2).setPreferredWidth(80);
        TblShow.getColumnModel().getColumn(3).setPreferredWidth(100);
        TblShow.getColumnModel().getColumn(4).setPreferredWidth(100);
        TblShow.getColumnModel().getColumn(5).setPreferredWidth(80);
    }

